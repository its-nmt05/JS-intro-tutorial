const month = 3

switch (month) {
    case 1:
        console.log("january");
        break;
    case 2:
        console.log("feb");
        break;
    case 3:
        console.log("march");
        break;
    case 4:
        console.log("april");
        break;

    default:
        console.log("no month matched");
        break;
}

/*
    The purpose of the break keyword is to break the flow of execution.
    Without the break statement, the control of the program will execute all
    the remaining case blocks until another break statment is encountered.
*/

